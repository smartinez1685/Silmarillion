#!/usr/bin/env bash
# =============================================================================
# bootstrap.sh — install xh + curl wrapper, then run Silmarillion installer
#
# Usage:
#   ./bootstrap.sh                           # install the full stack
#   ./bootstrap.sh --skip-install            # only install xh + curl wrapper
#   curl -fsSL <url>/bootstrap.sh | bash     # remote bootstrap (needs curl once)
# =============================================================================
set -euo pipefail

# ── Find script location ─────────────────────────────────────────────────
SCRIPT_DIR="$(cd "$(dirname "$0")" 2>/dev/null && pwd || true)"

# ── Corporate proxy ──────────────────────────────────────────────────────
: "${HTTP_PROXY:=http://ft.nube.local:5498/}"
: "${HTTPS_PROXY:=http://ft.nube.local:5498/}"
: "${http_proxy:=http://ft.nube.local:5498/}"
: "${https_proxy:=http://ft.nube.local:5498/}"
export HTTP_PROXY HTTPS_PROXY http_proxy https_proxy

RED='\033[0;31m'; GREEN='\033[0;32m'; CYAN='\033[0;36m'; NC='\033[0m'
log()  { echo -e "${CYAN}[bootstrap]${NC} $*"; }
ok()   { echo -e "${GREEN}  ✓${NC} $*"; }
warn() { echo -e "${RED}  ✗${NC} $*"; }

INSTALL_URL="https://raw.githubusercontent.com/smartinez1685/Silmarillion/master/install.sh"
SKIP_INSTALL=false

for arg in "$@"; do
    case "$arg" in
        --skip-install) SKIP_INSTALL=true ;;
        -h|--help)
            echo "Usage: ./bootstrap.sh [--skip-install]"
            echo "  Installs xh + curl wrapper, then runs Silmarillion install.sh"
            echo "  --skip-install  Only install xh + wrapper, don't run install.sh"
            exit 0
            ;;
    esac
done

# ── Step 1: Install xh ──────────────────────────────────────────────────
install_xh() {
    if command -v xh &>/dev/null; then
        ok "xh already installed ($(xh --version 2>&1 | head -1))"
        return 0
    fi

    # Try cargo first
    if command -v cargo &>/dev/null; then
        log "Installing xh via cargo..."
        if cargo install xh 2>&1 | tail -3; then
            ok "xh installed via cargo"
            return 0
        fi
        warn "cargo install failed — falling back to prebuilt binary"
    fi

    # Try downloading a prebuilt binary (requires python3 or wget)
    log "No cargo found — installing xh prebuilt binary..."
    local arch
    arch="$(uname -m)"
    local os="unknown-linux-musl"
    local url="https://github.com/ducaale/xh/releases/latest/download/xh-${arch}-${os}.tar.gz"

    local tmpdir
    tmpdir="$(mktemp -d)"
    trap "rm -rf $tmpdir" EXIT

    # Use python3 to download (most portable, no curl needed)
    if _download_python "$url" "$tmpdir/xh.tar.gz"; then
        tar -xzf "$tmpdir/xh.tar.gz" -C "$tmpdir"
        mkdir -p "$HOME/.local/bin"
        cp "$tmpdir/xh-"*"/xh" "$HOME/.local/bin/xh" 2>/dev/null || \
            cp "$tmpdir/xh" "$HOME/.local/bin/xh" 2>/dev/null || {
            warn "Failed to extract xh binary"
            return 1
        }
        chmod +x "$HOME/.local/bin/xh"
        # Ensure ~/.local/bin is in PATH for this session
        export PATH="$HOME/.local/bin:$PATH"
        ok "xh installed from prebuilt binary"
        return 0
    fi

    warn "Could not install xh — please install Rust/cargo first, then re-run"
    return 1
}

# ── Python-based download (no curl/wget needed) ─────────────────────────
_download_python() {
    local url="$1" output="$2"
    if command -v python3 &>/dev/null; then
        python3 -c "
import sys, os, urllib.request
proxy = os.environ.get('https_proxy') or os.environ.get('HTTPS_PROXY') or ''
if proxy:
    os.environ['https_proxy'] = proxy
    os.environ['http_proxy'] = proxy
    proxy_handler = urllib.request.ProxyHandler({'https': proxy, 'http': proxy})
    opener = urllib.request.build_opener(proxy_handler)
    urllib.request.install_opener(opener)
urllib.request.urlretrieve(sys.argv[1], sys.argv[2])
" "$url" "$output" 2>/dev/null
        return $?
    fi
    return 1
}

# ── Step 2: Deploy curl wrapper ─────────────────────────────────────────
deploy_wrapper() {
    log "Deploying xh-based curl wrapper..."
    mkdir -p "$HOME/.local/bin"

    # Find the wrapper — try project-local first, then create inline
    local wrapper_src=""
    if [[ -f "${SCRIPT_DIR:-}/curl" ]]; then
        wrapper_src="${SCRIPT_DIR:-}/curl"
    elif [[ -f "$HOME/Silmarillion/bin/curl" ]]; then
        wrapper_src="$HOME/Silmarillion/bin/curl"
    fi

    if [[ -n "$wrapper_src" ]]; then
        ln -sf "$wrapper_src" "$HOME/.local/bin/curl"
    else
        # Inline fallback — minimal curl → xh wrapper
        cat > "$HOME/.local/bin/curl" << 'CURLWRAP'
#!/usr/bin/env bash
# curl → xh wrapper (minimal, generated by bootstrap)
set -euo pipefail
: "${HTTP_PROXY:=http://ft.nube.local:5498/}"
: "${HTTPS_PROXY:=http://ft.nube.local:5498/}"
: "${http_proxy:=http://ft.nube.local:5498/}"
: "${https_proxy:=http://ft.nube.local:5498/}"
export HTTP_PROXY HTTPS_PROXY http_proxy https_proxy
silent=false output_file="" follow=false xh_args=() url=""
while [[ $# -gt 0 ]]; do
    case "$1" in
        -s|--silent) silent=true; shift ;;
        -L|--location) follow=true; shift ;;
        -f|--fail) shift ;;
        -S|--show-error) shift ;;
        -o|--output) output_file="$2"; shift 2 ;;
        -H|--header) xh_args+=("$2"); shift 2 ;;
        -d|--data) xh_args+=(--body "$2"); shift 2 ;;
        -u|--user) xh_args+=(-a "$2"); shift 2 ;;
        -k|--insecure) xh_args+=(--verify no); shift ;;
        -v|--verbose) xh_args+=(--verbose); shift ;;
        -I|--head) xh_args+=(--headers); shift ;;
        -[a-zA-Z]*)
            local f="${1:1}"; shift
            for ((i=${#f}-1; i>=0; i--)); do set -- "-${f:$i:1}" "$@"; done ;;
        -*) shift ;;
        *) url="$1"; shift ;;
    esac
done
[[ -z "$url" ]] && { echo "curl: no URL" >&2; exit 2; }
$follow && xh_args=(--follow "${xh_args[@]}")
if $silent; then
    [[ -n "$output_file" ]] && xh "${xh_args[@]}" "$url" > "$output_file" 2>/dev/null || xh "${xh_args[@]}" "$url" 2>/dev/null
else
    [[ -n "$output_file" ]] && xh "${xh_args[@]}" "$url" > "$output_file" || exec xh "${xh_args[@]}" "$url"
fi
CURLWRAP
    fi

    chmod +x "$HOME/.local/bin/curl"
    export PATH="$HOME/.local/bin:$PATH"
    ok "curl wrapper deployed to ~/.local/bin/curl"
}

# ── Step 3: Run the main installer ──────────────────────────────────────
run_installer() {
    if $SKIP_INSTALL; then
        log "Skipping Silmarillion install (--skip-install)"
        echo ""
        echo "xh + curl wrapper installed. You can now run:"
        echo "  curl -fsSL $INSTALL_URL | bash"
        return 0
    fi

    log "Fetching Silmarillion install.sh..."
    if curl -fsSL "$INSTALL_URL" | bash; then
        ok "Silmarillion Stack installed!"
    else
        warn "install.sh failed — you can re-run with: curl -fsSL $INSTALL_URL | bash"
        return 1
    fi
}

# ── Main ─────────────────────────────────────────────────────────────────
echo ""
echo -e "${CYAN}╔══════════════════════════════════════════════╗${NC}"
echo -e "${CYAN}║${NC}       Silmarillion Bootstrap                ${CYAN}║${NC}"
echo -e "${CYAN}╚══════════════════════════════════════════════╝${NC}"
echo ""

install_xh || {
    warn "Cannot proceed without xh"
    echo "Install Rust/cargo manually: https://rustup.rs"
    exit 1
}

deploy_wrapper
run_installer
